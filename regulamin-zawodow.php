<h3>12. Przerywanie pojedynku</h3>
<ul>
<li>a) Sędzia i lekarz ringowy są jedynymi osobami upoważnionymi do wejścia na pole walki w</li>
</ul>
<p>dowolnym momencie podczas walki, a sędzia jest jedyną osobą upoważnioną do zatrzymania</p>
<p>walki.</p>
<h3>13. Sędziowanie punktowe</h3>
<ul>
<li>a) Efektywne uderzanie/skuteczne zapasy należy uznać za priorytetowe w ocenianiu rundy.</li>
</ul>
<p>Agresywność powinna być brana pod uwagę tylko gdy sędzia nie dostrzega żadnej przewagi</p>
<p>jednego z zawodników w efektywnym uderzaniu/skutecznych zapasach. Kontrola ringu</p>
<p>powinna być brana pod uwagę tylko, gdy wszystkie inne składowe, w przypadku obydwu</p>
<p>zawodników, są równe w 100%.</p>
<ul>
<li>b) Efektywne uderzanie / skuteczny grappling</li>
</ul>
<p>Dozwolone uderzenia, które w sposób natychmiastowy lub poprzez kumulacje uderzeń mogą</p>
<p>przyczynić się do zakończenia walki.</p>
<p>Pojedynczy dozwolony cios, który ma natychmiastowy wpływ na uderzanego przeciwnika jest</p>
<p>oceniany wyżej niż kumulacja uderzeń, która wywoła podobny skutek.</p>
<p>Pomyślne wykonanie obalenia, próba poddania, zmiana pozycji i osiągnięcie korzystnej</p>
<p>pozycji, które w sposób natychmiastowy lub w wyniku kumulacji przybliżają zawodnika do</p>
<p>potencjalnej wygranej przed czasem są oceniane wyżej, niż łączny wpływ tych technik nie</p>
<p>dający możliwości zakończenia pojedynku przed czasem.</p>
<p>Na skuteczne obalenie nie składa się tylko zmiana pozycji, ale zapewnienie sobie pozycji do</p>
<p>ataku po obaleniu przeciwnika.</p>
<p>Próby poddania przeciwnika, które widocznie go osłabiają i powodują wyraźne zmęczenie w</p>
<p>trakcie obrony i ucieczki, będą oceniane wyżej niż te, z których mógł się łatwo wybronić lub</p>
<p>uciec bez zauważalnego zmęczenia.</p>
<p>Wysokie wyniesienia, mocne rzuty i sprowadzenia, oceniane są wyżej niż wywrócenie lub</p>
<p>przyciśnięcie do maty.</p>
<ul>
<li>c) Powyższe zasady będą kluczowe w przypadku przeważającej większości decyzji</li>
</ul>
<p>dotyczących</p>
<p>punktacji danej rundy. Kolejne dwa kryteria powinny być stosowane tylko w przypadku, gdy</p>
<p>Efektywne uderzanie/skuteczny grappling są na w 100% identycznym poziomie przez cały czas</p>
<p>trwania rundy.</p>
<ul>
<li>d) Efektywna Agresja:</li>
</ul>
<p>Agresywnie podejmowanie próby zakończenia walki. Kluczowym terminem jest „efektywny”.</p>
<p>Ściganie przeciwnika bez skutecznego wyniku lub wpływu nie powinno uwzględniać się w</p>
<p>ocenach sędziów</p>
<ul>
<li>e) Kontrola pola walki</li>
</ul>
<p>Kontrolę obszaru walki ocenia się poprzez określenie, kto dyktuje tempo, miejsce i pozycję</p>
<p>walki.</p>
<ul>
<li>f) Wszystkie walki będą oceniane i punktowane przez trzech sędziów punktowych</li>
<li>g) System 10 punktów będzie standardowym systemem do punktowania walk</li>
<li>h) Punktowanie rundy 10-10</li>
</ul>
<p>Występuje, gdy nie ma możliwości wskazania zawodnika, który w czasie trwania całej rundy,</p>
<p>uzyskał jakąkolwiek przewagę nad swoim przeciwnikiem.</p>
<p>Punktacja 10 – 10 w MMA powinna być stosowana w wyjątkowych przypadkach i nie może</p>
<p>być dla sędziów wymówką, usprawiedliwiającą brak umiejętności dostrzeżenia różnicy</p>
<p>pomiędzy zawodnikami w trakcie rundy</p>
<ul>
<li>i) Punktowanie rundy 10–9</li>
</ul>
<p>Gdy zawodnik nieznacznie wygrywa, notując większą liczbę efektywnych ciosów zgodnych z</p>
<p>zasadami, technik parterowych i innych</p>
<p>Kiedy zawodnik przewalczył rundę w gardzie i żaden z walczących nie miał przewagi w stójce</p>
<p>ani w parterze, wygrywa rundę ten, komu udało się obalić przeciwnika w sposób zgodny z</p>
<p>zasadami.</p>
<p>Runda punktowana 10-9 odzwierciedla wyrównaną rywalizację z niewielką przewagą jednego</p>
<p>z rywali</p>
<ul>
<li>j) Punktowanie rundy 10-8</li>
</ul>
<p>Kiedy zawodnik dominuje w przeważającej części rundy zadając więcej ciosów i stosując</p>
<p>więcej technik parterowych</p>
<p>Kiedy zawodnik skutecznie neutralizuje ataki swojego przeciwnika nokdaunując go ciosami</p>
<p>zgodnymi z zasadami, zagrażając próbami poddań, wyprowadzając efektywne ciosy zarówno</p>
<p>w stójce jak i w parterze</p>
<ul>
<li>k) Punktowanie rundy 10–7</li>
</ul>
<p>Kiedy zawodnik zupełnie dominuje rundę w stójce lub w parterze.</p>
<p>Kiedy zawodnik absolutnie neutralizuje działania swojego przeciwnika nokdaunując go</p>
<p>ciosami zgodnymi z zasadami, zagrażając próbami poddań, wyprowadzając efektywne ciosy</p>
<p>zarówno w stójce jak i w parterze</p>
<p>Punktacja 10-7 stosowana jest w rzadkich przypadkach’</p>
<h3>14. Ostrzeżenia</h3>
<ul>
<li>a) Zawodnik otrzyma pojedyncze ostrzeżenie tylko w poniższych sytuacjach:</li>
<li>• Trzymanie się lub łapanie siatki</li>
<li>• Trzymanie przeciwnika za spodenki, rękawice, nagolenniki, rashguard</li>
<li>• Kierowanie dłoni z wyprostowanymi palcami równolegle do maty w kierunku twarzy</li>
</ul>
<p>przeciwnika.</p>
<h3>15. Faule</h3>
<ul>
<li>a) Następujące zachowania uznaje się za faule w pojedynku mieszanych sztuk walki:</li>
<li>• Uderzanie głową</li>
<li>• Atakowanie oczu w każdy sposób</li>
<li>• Gryzienie</li>
<li>• Plucie na rywala</li>
<li>• Ciągnięcie za włosy</li>
<li>• Wkładanie palców do buzi rywala</li>
<li>• Wszelkiego rodzaju ataki na krocze</li>
<li>• Wkładanie palców w otwory, otarcia lub rozcięcia rywala</li>
<li>• Dźwignie na małe stawy</li>
<li>• Pionowe uderzanie czubkiem łokcia (z godziny 12 na 6)</li>
<li>• Ciosy w kręgosłup (obejmuje kość ogonową) i tył głowy</li>
<li>• Wszelkie ataki na krtań oraz łapanie za tchawicę</li>
