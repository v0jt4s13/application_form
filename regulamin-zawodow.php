<h3>17. Kontuzje spowodowane dozwolonymi akcjami i faulami</h3>
<ul>
<li>a) Jeśli kontuzja powstała w czasie walki w wyniku dozwolonej techniki jest na tyle poważna,</li>
</ul>
<p>aby</p>
<p>przerwać pojedynek, kontuzjowany zawodnik przegrywa przez techniczny nokaut.</p>
<ul>
<li>b) Kontuzje po faulach zamierzonych:</li>
</ul>
<p>Jeśli kontuzja powstała w czasie walki w wyniku umyślnego, według oceny sędziego</p>
<p>ringowego, faulu jest na tyle poważna, aby przerwać pojedynek, zawodnik, który faulował</p>
<p>przegrywa przez dyskwalifikację.</p>
<p>Jeśli kontuzja powstała w czasie walki w wyniku umyślnego, według oceny sędziego</p>
<p>ringowego, faulu pozwala sfaulowanemu zawodnikowi na kontynuowanie pojedynku, sędzia</p>
<p>ringowy informuje osobę odpowiedzialną za karty sędziowskie o odjęciu dwóch punktów na</p>
<p>kartach zawodnika, który popełnił faul.</p>
<p>Jeśli kontuzja powstała w czasie walki w wyniku umyślnego, według oceny sędziego</p>
<p>ringowego, faulu pozwala sfaulowanemu zawodnikowi na kontynuowanie pojedynku, jednak</p>
<p>w późniejszym czasie nie pozwala mu na dalszą walkę, kontuzjowany zawodnik wygrywa przez</p>
<p>techniczną decyzję, jeśli do tego momentu prowadził na kartach sędziowskich. Jeśli</p>
<p>kontuzjowany zawodnik remisował lub przegrywał na kartach sędziowskich w momencie</p>
<p>przerwania walki, walka zostaje uznana za techniczny remis.</p>
<p>Jeśli zawodnik dozna kontuzji próbując sfaulować rywala, sędzia ringowy nie podejmuje</p>
<p>żadnej akcji na korzyść zawodnika, a kontuzja jest traktowana tak jakby powstała w wyniku</p>
<p>dozwolonej akcji.</p>
<ul>
<li>c) Kontuzje po faulach nieumyślnych:</li>
</ul>
<p>Jeśli kontuzja powstała w czasie walki w wyniku nieumyślnego, według oceny sędziego</p>
<p>ringowego, faulu jest na tyle poważna, aby natychmiast przerwać pojedynek, walka zostaje</p>
<p>uznana za no contest jeśli zostanie przerwana przed upływem dwóch rund w trzyrundowym</p>
<p>pojedynku lub przed upływem trzech rund w pojedynku pięciorundowym.</p>
<p>Jeśli kontuzja powstała w czasie walki w wyniku nieumyślnego, według oceny sędziego</p>
<p>ringowego, faulu jest na tyle poważna, aby natychmiast przerwać pojedynek, walka zostaje</p>
<p>uznana za techniczną decyzję na korzyść zawodnika, który prowadził na kartach punktowych</p>
<p>w momencie przerwania pojedynku, tylko wtedy, gdy przerwanie pojedynku miało miejsce po</p>
<p>zakończeniu dwóch rund w pojedynku trzyrundowym i trzech rund w pojedynku</p>
<p>pięciorundowym.</p>
<p>Nieskończone rundy w przypadku sytuacji opisanej w drugim podpunkcie punktu c) nie będą</p>
<p>punktowane</p>
<p>Jeśli dojdzie do kontuzji opisanej w drugim podpunkcie punktu c), a sędzia ukarze jednego z</p>
<p>zawodników odjęciem punktów, punkty te muszą być odjęte od końcowego wyniku.</p>
