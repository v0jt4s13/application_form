<h3>Artykuł 1. Definicja.</h3>
<p>K1 Rulez to formuła kickboxingu, w której intencją zawodnika jest pokonanie przeciwnika przy</p>
<p>użyciu dozwolonych technik zadawanych z pełną siłą, w dozwolone obszary trafień przypełnej</p>
<p>koncentracji uderzającego. Można atakować przód i boki głowy oraz przód i boki tułowia.</p>
<p>Kopnięcia stopą i podudziem oraz uderzenia kolanami są dozwolone w nogę przeciwnika na</p>
<p>całej długości z włączeniem stawów: kolanowego i skokowego. Można też przytrzymać</p>
<p>przeciwnika za szyję lub ramiona jedną lub obiema rękami w celu wykonania uderzenia</p>
<p>kolanem.</p>
<h3>Artykuł 2. Ubiór zawodnika i sprzęt do walki K-1 Rulez.</h3>
<h3>Artykuł 2.1. Sprzęt do walki K-1 Rulez.</h3>
<ul>
<li>1.Kask.</li>
<li>2.Ochraniacz na zęby.</li>
<li>3.Ochraniacze na piersi (dla zawodniczek).</li>
<li>4.Owijki na dłonie (nie mogą być mocowane taśmą klejącą).</li>
<li>5.Rękawice o wadze 10 uncji.</li>
<li>6.Ochraniacz na krocze (obowiązkowy dla wszystkich).</li>
<li>7.Nagolenniki (odpowiednie dla K-1 Rulez); niedozwolone są nagolenniki typu „skarpeta”.</li>
<li>8.Owijki na stopy (dozwolone - muszą być zakryte ściągaczem stawu skokowego).</li>
<li>9.Ściągacz na staw skokowy (dozwolony).</li>
<li>10.Ochraniacz na podbicie stopy (odpowiedni dla K-1 Rulez łączony z nagolennikami).</li>
</ul>
<h3>Artykuł 2.2. Ubiór zawodnika w K-1 Rulez.</h3>
<ul>
<li>1.Spodenki dla mężczyzn (bez kieszonek, rzepów i widocznych sznurowadeł, zz widocznym</li>
</ul>
<p>pasem).</p>
<ul>
<li>2.Sportowy top lub koszulka „tank top” i spodenki dla kobiet (niedozwolone są spódnico-</li>
</ul>
<p>spodenki).</p>
<h3>Artykuł 3. Dozwolone obszary trafień w K-1 Rulez.</h3>
<ul>
<li>1.Zawodnik wykonujący prawidłowe techniki K-1 Rulez może atakować następujące części ciała</li>
</ul>
<p>przeciwnika:</p>
<ul>
<li>a)głowę (przód, boki),</li>
<li>b)korpus (przód, boki),</li>
<li>c)nogi (na całej długości łącznie ze stawami).</li>
</ul>
<h3>Artykuł 4. Dozwolone techniki w K-1 Rulez.</h3>
<ul>
<li>1.Techniki rąk, nóg i kolan muszą być używane równomiernie przez całą walkę. Wszystkie</li>
</ul>
<p>techniki muszą być używane z pełną mocą. Każda technika, która zostanie zablokowana, lub</p>
<p>tylko dotyka, ociera czy też popycha przeciwnika, nie będzie punktowana.</p>
<h3>Artykuł 4.1. Techniki ręczne (ciosy).</h3>
<ul>
<li>1. Proste.</li>
<li>2. Sierpowe.</li>
<li>3. Haki.</li>
<li>4. Uderzenia grzbietem pięści (tylko po obrocie).</li>
<li>5. Klincz (trwający maksymalnie 5 sekund, przygotowujący zawodnika do zadawania uderzeń –</li>
</ul>
<p>tzw. „aktywny klincz”).</p>
<h3>Artykuł 4.2. Techniki nożne.</h3>
<ul>
<li>1. Kopnięcia proste na tułów i głowę (atak na udo nie jest dozwolony).</li>
<li>2. Kopnięcia boczne na tułów i głowę (atak na udo nie jest dozwolony).</li>
<li>3. Kopnięcia okrężne.</li>
<li>4. Kopnięcia zahaczające (atak piętą jest dozwolony).</li>
<li>5. Kopnięcia opadające.</li>
<li>6. Kopnięcia z wyskoku.</li>
<li>7. Kopnięcie obrotowe na tułów i głowę (atak na udo nie jest dozwolony).</li>
