<p>ręcznych lub nożnych lub w celu sprowadzenia przeciwnika na podłogę ringu.</p>
<h3>Artykuł 4.4. Rzuty.</h3>
<ul>
<li>1. Niedozwolone są rzuty przeciwnika; zawodnik nie może rzucać przeciwnika wykorzystując</li>
</ul>
<p>tułów, biodra lub ramiona.</p>
