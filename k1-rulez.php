<li>a) uderzenia poniżej pasa, łokciami, barkami lub przedramionami,</li>
<li>b) uderzanie w plecy przeciwnika, w szyję, kark, gardło, tył głowy, z góry w obojczyki i okolice</li>
</ul>
<p>nerek,</p>
<ul>
<li>c) chwytanie nóg przeciwnika w celu wykonania technik ręcznych lub nożnych,</li>
<li>d) wykonywanie kopnięć prostych lub bocznych na przód uda, kolana lub podudzia (dotyczy to</li>
</ul>
<p>również ataków kolanem),</p>
<ul>
<li>e) wykonywanie więcej niż jednego ataku kolanem, po chwyceniu szyi lub ramion przeciwnika</li>
</ul>
<p>dwoma lub jedną ręką,</p>
<ul>
<li>f) wykonywanie technik nożnych po obrocie w tył, w przód lub w bok uda,</li>
