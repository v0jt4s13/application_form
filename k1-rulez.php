<li>1. Dozwolone jest atakowanie przeciwnika kolanem w następujące części ciała:</li>
<li>a)nogi,</li>
<li>b)korpus,</li>
<li>c)głowa.</li>
<li>2. Dozwolone jest chwytanie szyi przeciwnika jedną lub obiema rękami, aby natychmiast</li>
